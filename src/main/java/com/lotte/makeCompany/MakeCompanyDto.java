package com.lotte.makeCompany;

public class MakeCompanyDto {
	
	Integer mkId;
	String mkName;
	String mkCeo;
	
	
	public Integer getMkId() {
		return mkId;
	}
	public void setMkId(Integer mkId) {
		this.mkId = mkId;
	}
	public String getMkName() {
		return mkName;
	}
	public void setMkName(String mkName) {
		this.mkName = mkName;
	}
	public String getMkCeo() {
		return mkCeo;
	}
	public void setMkCeo(String mkCeo) {
		this.mkCeo = mkCeo;
	}
	
	

}
