package com.lotte.productClass;

public class ProductClassDto {
	
	Integer classId;
	String classBigName;
	String classSmallName;
	
	
	public Integer getClassId() {
		return classId;
	}
	public void setClassId(Integer classId) {
		this.classId = classId;
	}
	public String getClassBigName() {
		return classBigName;
	}
	public void setClassBigName(String classBigName) {
		this.classBigName = classBigName;
	}
	public String getClassSmallName() {
		return classSmallName;
	}
	public void setClassSmallName(String classSmallName) {
		this.classSmallName = classSmallName;
	}

}
